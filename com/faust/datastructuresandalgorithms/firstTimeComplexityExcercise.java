package com.faust.datastructuresandalgorithms;

public class firstTimeComplexityExcercise {

    private static void simpleFunction_1(int n) {
        System.out.println("Input argument is " + n); // O(1)
    }
    private static void simpleFunction_2(int[] arr) {
        System.out.println("\nSimple array.toString() " + arr.toString()); // O(1)
    }
}
